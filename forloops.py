#!/usr/bin/python

#!Creating for_loops

for letter in "python tutorial":
    print(letter)

#!with a variable
people = ["George", "Chris", "Ivy", "Nick"]
for name in people:
    print(name)

#!with indices
for index in range(10):
    print(index) #!note here its not going to print out 10 but rather is going to start from zero(0) to nine(9)

for indices in range(2, 10):
    print(indices) #!note here the numbers printed will start from 2 all the way to 9


